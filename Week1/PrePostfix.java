
public class PrePostfix {
    public static void main(String[] args) {
        
       // prefix operator
       int prefixValue=10;
       System.out.println("prefix value update the value in this line"+ ++prefixValue);
       System.out.println("it remain same in next line "+ prefixValue);
       //postfix operator
       int postFixValue=20;
       System.out.println("postfix value updates the value in next linme"+ postFixValue);
       System.out.println("the value change in this line "+ postFixValue);
    }
    
}
